
public abstract class Esempio {
	
	/*
	 * METODO ASTRATTO
	 * un metodo è astratto quando per definizione non implementa il suo comportamento 
	 */
	
	public abstract double calcolaArea();    // un metodo astratto non ha parentesi graffe quindi non ha istruzioni
	                                       // questo perchè vogliamo riscriverlo a seconda del caso
	
	                                       // basta che un solo metodo sia astratto affinche anche la classe sia astratta
	
	                                       // tutte le classi che implementano la classe astratta devono implementare il 
	                                       // suo metodo astratto
            

}
